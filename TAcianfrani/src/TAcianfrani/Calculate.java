package TAcianfrani;

public class Calculate {

    public double add(double var1, double var2){
        return var1+var2;
    }

    public double sub(double var1, double var2){
        return var1-var2;
    }

    public double div(double var1, double var2){
        return var1/var2;
    }

    public double mul(double var1, double var2){
        return var1*var2;
    }
}
